def get_luminance(color: tuple[int, int, int, int]) -> float:
    """Return the luminance value for a color, the luminance is how 'bright' the human eye perceives a color"""

    return color[0] * 0.299 + color[1] * 0.587 + color[2] * 0.114;

def get_color_names() -> list[str]:
    """Returns a list of all the names of the colors in this module"""

    return list(ColorList.keys());

def get_color(color_name: str) -> tuple[int, int, int, int]:
    """Returns a tuple containing the RGBA color values for the given color name"""

    return ColorList[color_name];

def get_colors(color_names: list[str]) -> list[tuple[int, int, int, int]]:
    """Returns a list of tuples containing the RGBA color values for the given color names"""

    return [ColorList[c] for c in color_names];

def get_dark_colors(threshold: int=127):
    """Returns a list of all the names of colors with less luminance than the threshold"""

    return [name for name, var in ColorList.items() if not name.startswith("__") and isinstance(var, tuple) and (get_luminance(var) < threshold)];

def get_light_colors(threshold: int=129):
    """Returns a list of all the names of colors with more luminance than the threshold"""

    return [name for name, var in ColorList.items() if not name.startswith("__") and isinstance(var, tuple) and (get_luminance(var) > threshold)];

